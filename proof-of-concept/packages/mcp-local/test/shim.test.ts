// 1. cuit__generate_spec_from_session — Jam session normalization
// ---------------------------------------------------------------------------

describe('cuit__generate_spec_from_session', () => {
  test('normalizes a raw Jam session and returns an ok AX envelope with serialized spec', async () => {
    const result = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });

    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();

    const spec = result.data!.spec;
    expect(spec.testName).toBe(SPEC_GEN_TEST_NAME);

    // Verify dispatchDrag primitive exists with the independently-computed dx
    const dragPrim = spec.primitives.find((p) => p.kind === 'dispatchDrag');
    expect(dragPrim).toBeDefined();
    expect(dragPrim).toMatchObject({
      kind: 'dispatchDrag',
      dx: SEG_COLLISION_EXPECTED_DX,
    });

    // Verify assertStateEquals exists for ASSERT_PATH
    const assertPrim = spec.primitives.find((p) => p.kind === 'assertStateEquals');
    expect(assertPrim).toBeDefined();
    expect(assertPrim).toMatchObject({
      kind: 'assertStateEquals',
      path: ASSERT_PATH,
      value: SEG_COLLISION_EXPECTED_DX,
    });

    // serialized must reference @haywork/cuit-harness (not an empty string, not a placeholder)
    const serialized = result.data!.serialized;
    expect(typeof serialized).toBe('string');
    expect(serialized.length).toBeGreaterThan(0);
    expect(serialized).toContain("from '@haywork/cuit-harness'");

    // next_actions must reference the run-spec tool
    expect(result.next_actions.some((a) => a.includes('cuit__run_spec'))).toBe(true);
  });

  test('passes a pre-normalized cuit session through without re-normalizing (resize-then-drag shape)', async () => {
    // TUR fixture vendor='cuit', already-typed SessionEvent[]
    // This shape has 1 nav + 12 snapshots + 4 pointer events
    const result = await generateSpecTool({
      session: TUR_FIXTURE,
    });

    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();

    const spec = result.data!.spec;

    // The nav url from the cuit session must flow through to spec.url
    expect(spec.url).toBe(TUR_NAV_URL);

    // The drag must reflect the cuit-session pointer deltas
    const dragPrim = spec.primitives.find((p) => p.kind === 'dispatchDrag');
    expect(dragPrim).toBeDefined();
    expect(dragPrim).toMatchObject({
      kind: 'dispatchDrag',
      dx: TUR_EXPECTED_DX,
    });
  });

  test('returns outcome=error for a session with empty events array and does NOT throw', async () => {
    const emptySession: RawJamSession = {
      sessionId: 'empty-test',
      vendor: 'jam',
      createdAt: '2026-01-01T00:00:00Z',
      url: 'http://localhost:5173/',
      events: [],
    };

    // The tool must NOT throw — any thrown error is itself a test failure.
    // We call the tool inside a try/catch and re-throw unexpected errors so
    // the assertion message is clear when the implementation is missing.
    let result: AxEnvelope<unknown>;
    try {
      result = await generateSpecTool({ session: emptySession });
    } catch (err) {
      throw new Error(
        `generateSpecTool threw instead of returning an error envelope: ${String(err)}`,
      );
    }

    expect(result!.outcome).toBe('error');
    expect(typeof result!.summary).toBe('string');
    expect(result!.summary.length).toBeGreaterThan(0);
    expect(result!.summary.toLowerCase()).toMatch(/empty|no events/);
    expect(result!.data).toBeNull();
    expect(Array.isArray(result!.next_actions)).toBe(true);
    expect(result!.next_actions.length).toBeGreaterThan(0);
  });
});

// ---------------------------------------------------------------------------
// 2. cuit__run_spec — GREEN path (snapshot matches expected)
// ---------------------------------------------------------------------------

describe('cuit__run_spec', () => {
  test('reports GREEN when injected snapshotProvider matches expectedFinalState', async () => {
    // First generate a spec to have a real contract to run against
    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    expect(genResult.outcome).toBe('ok');
    const spec = genResult.data!.spec;

    // Build the provider from the spec's own contract — not a magic literal
    const snapshotProvider = providerFromExpected(spec);

    const result = await runSpecTool({ spec, snapshotProvider });

    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();
    expect(result.data!.passed).toBe(true);
    expect(result.data!.mismatches).toHaveLength(0);
    expect(typeof result.summary).toBe('string');
    expect(result.summary.toLowerCase()).toMatch(/pass/);
  });

  test('reports RED with precise field-level mismatch when snapshot diverges', async () => {
    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    expect(genResult.outcome).toBe('ok');
    const spec = genResult.data!.spec;

    // Parameterized delta — differ from the spec's expected value by a known offset
    const DELTA = 15;
    const snapshotProvider = providerWithDelta(spec, DELTA);

    const result = await runSpecTool({ spec, snapshotProvider });

    // The tool ran successfully — a red is not a tool failure
    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();
    expect(result.data!.passed).toBe(false);
    expect(result.data!.mismatches).toHaveLength(1);

    // The mismatch oracle is computed from the spec contract + the delta,
    // NOT read back from the tool output.
    const expectedSpecValue = spec.expectedFinalState[0]!.value as number;
    const expectedMismatch: StateMismatch = {
      path: ASSERT_PATH,
      expected: expectedSpecValue,
      actual: expectedSpecValue + DELTA,
    };
    expect(result.data!.mismatches[0]).toEqual(expectedMismatch);
  });

  test('returns an AX envelope (not throw) when spec has no expected state entries', async () => {
    const emptySpec: GeneratedSpec = {
      testName: 'stub',
      url: '',
      primitives: [],
      expectedFinalState: [],
    };

    // The tool must never throw — wrap in try/catch so a throw surfaces clearly.
    let result: AxEnvelope<unknown>;
    try {
      result = await runSpecTool({ spec: emptySpec });
    } catch (err) {
      throw new Error(
        `runSpecTool threw instead of returning an envelope: ${String(err)}`,
      );
    }

    // An empty spec with no expectedFinalState should pass vacuously or report
    // explicitly — either way it must be a well-formed AX envelope.
    expect(['ok', 'error']).toContain(result!.outcome);
    expect(typeof result!.summary).toBe('string');
    expect(Array.isArray(result!.next_actions)).toBe(true);
  });
});

