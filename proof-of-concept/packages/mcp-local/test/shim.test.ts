// 3. cuit__close_loop — end-to-end, zero network, chained AX envelope
// ---------------------------------------------------------------------------

describe('cuit__close_loop', () => {
  test('runs generate→run→report end-to-end on segment-collision fixture with NO network', async () => {
    // Guard: spy on globalThis.fetch BEFORE calling the tool
    const fetchSpy = vi.spyOn(globalThis, 'fetch').mockImplementation(() => {
      throw new Error('cuit__close_loop must not call fetch — zero network allowed');
    });

    // Independently compute the expected normalizedEventCount using the adapter.
    // This is the oracle; the tool result must match this count exactly.
    const independentlyNormalized = normalizeJamSession(SEG_COLLISION_FIXTURE);
    const expectedNormalizedEventCount = independentlyNormalized.length;

    // Generate a spec independently to build the snapshotProvider oracle
    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    expect(genResult.outcome).toBe('ok');
    const specForProvider = genResult.data!.spec;
    const snapshotProvider = providerFromExpected(specForProvider);

    const result = await closeLoopTool({
      session: SEG_COLLISION_FIXTURE,
      snapshotProvider,
    });

    // The fetch spy must record zero calls
    expect(fetchSpy).not.toHaveBeenCalled();

    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();

    const data = result.data!;

    // All four pipeline steps must be present in order
    expect(data.steps).toEqual(['normalize', 'generate', 'run', 'report']);

    expect(data.specGenerated).toBe(true);
    expect(data.passed).toBe(true);

    // normalizedEventCount must equal the independently computed value — not a magic number
    expect(data.normalizedEventCount).toBe(expectedNormalizedEventCount);

    // next_actions must be an array of strings
    expect(Array.isArray(result.next_actions)).toBe(true);
    expect(result.next_actions.length).toBeGreaterThan(0);
  });

  test('returns outcome=ok with passed=false (RED) when snapshot diverges — bug-reproduction is a valid loop outcome', async () => {
    // A divergent snapshot provider: every path returns a different value
    const redProvider: SnapshotProvider = () => ({
      [ASSERT_PATH]: -9999,
    });

    const result = await closeLoopTool({
      session: SEG_COLLISION_FIXTURE,
      snapshotProvider: redProvider,
    });

    // Reproducing a RED is a valid outcome — outcome must be 'ok', not 'error'
    expect(result.outcome).toBe('ok');
    expect(result.data).not.toBeNull();
    expect(result.data!.passed).toBe(false);
    expect(result.data!.mismatches.length).toBeGreaterThan(0);
    // next_actions must instruct the agent to fix the issue
    expect(Array.isArray(result.next_actions)).toBe(true);
    expect(result.next_actions.length).toBeGreaterThan(0);
  });
});

