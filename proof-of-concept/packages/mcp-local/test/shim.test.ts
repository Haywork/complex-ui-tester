// 5. AX envelope contract — all tools, every call produces a well-formed envelope
// ---------------------------------------------------------------------------

describe('AX envelope contract', () => {
  const AX_KEYS = ['outcome', 'summary', 'data', 'next_actions'] as const;
  const VALID_OUTCOMES = new Set(['ok', 'error']);

  function assertWellFormedEnvelope(envelope: AxEnvelope<unknown>, label: string): void {
    for (const key of AX_KEYS) {
      expect(
        Object.prototype.hasOwnProperty.call(envelope, key),
        `${label}: missing key "${key}"`,
      ).toBe(true);
    }
    expect(
      VALID_OUTCOMES.has(envelope.outcome),
      `${label}: outcome must be 'ok' or 'error', got "${String(envelope.outcome)}"`,
    ).toBe(true);
    expect(
      typeof envelope.summary,
      `${label}: summary must be a string`,
    ).toBe('string');
    expect(
      Array.isArray(envelope.next_actions),
      `${label}: next_actions must be an array`,
    ).toBe(true);
    expect(
      envelope.next_actions.every((a) => typeof a === 'string'),
      `${label}: every next_action must be a string`,
    ).toBe(true);
  }

  test('generateSpecTool — ok path returns well-formed AX envelope', async () => {
    const result = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    assertWellFormedEnvelope(result, 'generateSpecTool[ok]');
  });

  test('generateSpecTool — error path returns well-formed AX envelope with null data', async () => {
    const emptySession: RawJamSession = {
      sessionId: 'empty',
      vendor: 'jam',
      createdAt: '2026-01-01T00:00:00Z',
      url: '',
      events: [],
    };
    const result = await generateSpecTool({ session: emptySession });
    assertWellFormedEnvelope(result, 'generateSpecTool[error]');
    expect(result.outcome).toBe('error');
    expect(result.data).toBeNull();
    // summary must mention the root cause
    expect(result.summary.length).toBeGreaterThan(0);
  });

  test('runSpecTool — ok path returns well-formed AX envelope', async () => {
    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    const spec = genResult.data!.spec;
    const result = await runSpecTool({
      spec,
      snapshotProvider: providerFromExpected(spec),
    });
    assertWellFormedEnvelope(result, 'runSpecTool[ok]');
  });

  test('closeLoopTool — ok path returns well-formed AX envelope', async () => {
    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    const spec = genResult.data!.spec;
    const result = await closeLoopTool({
      session: SEG_COLLISION_FIXTURE,
      snapshotProvider: providerFromExpected(spec),
    });
    assertWellFormedEnvelope(result, 'closeLoopTool[ok]');
  });

  test('detectAppShapeTool — ok path returns well-formed AX envelope', async () => {
    const dir = join(tmpdir(), `cuit-envelope-test-${Date.now()}`);
    mkdirSync(dir, { recursive: true });
    writeFileSync(
      join(dir, 'package.json'),
      JSON.stringify({ name: 'test-app', dependencies: { react: '^18.3.1' } }),
    );
    const result = await detectAppShapeTool({ dir });
    assertWellFormedEnvelope(result, 'detectAppShapeTool[ok]');
  });

  test('every tool envelope has exactly the four required keys — no extra, none missing', async () => {
    // Collect one envelope per tool to check key set exhaustively
    const dir = join(tmpdir(), `cuit-keys-test-${Date.now()}`);
    mkdirSync(dir, { recursive: true });
    writeFileSync(
      join(dir, 'package.json'),
      JSON.stringify({ name: 'keys-test', dependencies: { react: '^18.0.0' } }),
    );

    const genResult = await generateSpecTool({ session: SEG_COLLISION_FIXTURE });
    const spec = genResult.outcome === 'ok' ? genResult.data!.spec : null;

    const envelopes: Array<[string, AxEnvelope<unknown>]> = [
      ['generateSpecTool', genResult],
      ['detectAppShapeTool', await detectAppShapeTool({ dir })],
    ];

    if (spec !== null) {
      envelopes.push([
        'runSpecTool',
        await runSpecTool({ spec, snapshotProvider: providerFromExpected(spec) }),
      ]);
      envelopes.push([
        'closeLoopTool',
        await closeLoopTool({ session: SEG_COLLISION_FIXTURE, snapshotProvider: providerFromExpected(spec) }),
      ]);
    }

    for (const [label, envelope] of envelopes) {
      const keys = Object.keys(envelope).sort();
      expect(keys, `${label}: unexpected key set`).toEqual(
        [...AX_KEYS].sort(),
      );
    }
  });
});

