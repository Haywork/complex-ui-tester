const HERE = dirname(fileURLToPath(import.meta.url));
const FIXTURES_DIR = join(HERE, '../../../fixtures');

function loadSegmentCollision(): RawJamSession {
  return JSON.parse(
    readFileSync(join(FIXTURES_DIR, 'segment-collision.json'), 'utf-8'),
  ) as RawJamSession;
}

function loadTranslateUiReact(): { vendor: 'cuit'; events: SessionEvent[]; url: string } {
  const raw = JSON.parse(
    readFileSync(join(FIXTURES_DIR, 'translate-ui-react-drag-after-resize.json'), 'utf-8'),
  ) as { vendor: string; events: SessionEvent[]; url: string };
  return raw as { vendor: 'cuit'; events: SessionEvent[]; url: string };
}

// ---------------------------------------------------------------------------
// Computed fixture constants — derived independently, NOT from the tool output.
// These values are computed from the raw fixture data before any tool call
// so the tests never use the function under test as its own oracle.
// ---------------------------------------------------------------------------

// segment-collision: pointer-down x=50, pointer-up x=150 → dx=100
// Both values are read from the fixture file directly.
const SEG_COLLISION_FIXTURE = loadSegmentCollision();
const SEG_COLLISION_POINTER_DOWN_X = (() => {
  const pd = SEG_COLLISION_FIXTURE.events.find(
    (e): e is Record<string, unknown> =>
      (e as Record<string, unknown>)['type'] === 'pointer' &&
      (e as Record<string, unknown>)['phase'] === 'down',
  );
  return pd?.['x'] as number;
})();
const SEG_COLLISION_POINTER_UP_X = (() => {
  const pu = [...SEG_COLLISION_FIXTURE.events]
    .reverse()
    .find(
      (e): e is Record<string, unknown> =>
        (e as Record<string, unknown>)['type'] === 'pointer' &&
        (e as Record<string, unknown>)['phase'] === 'up',
    );
  return pu?.['x'] as number;
})();
const SEG_COLLISION_EXPECTED_DX =
  SEG_COLLISION_POINTER_UP_X - SEG_COLLISION_POINTER_DOWN_X;

// translate-ui-react: pointer-down x=404, pointer-up x=424 → dx=20
// targetName=seg-1, nav url
const TUR_FIXTURE = loadTranslateUiReact();
const TUR_POINTER_DOWN_X = (() => {
  const pd = TUR_FIXTURE.events.find(
    (e) => e.type === 'pointer' && (e as { phase: string }).phase === 'down',
  );
  return (pd as { x: number } | undefined)?.x ?? 0;
})();
const TUR_POINTER_UP_X = (() => {
  const pu = [...TUR_FIXTURE.events]
    .reverse()
    .find(
      (e) => e.type === 'pointer' && (e as { phase: string }).phase === 'up',
    );
  return (pu as { x: number } | undefined)?.x ?? 0;
})();
const TUR_EXPECTED_DX = TUR_POINTER_UP_X - TUR_POINTER_DOWN_X;
const TUR_NAV_URL = TUR_FIXTURE.url;

// The test name is dynamically generated by spec-gen from the interaction shape.
// For segment-collision: pointer-down at seg-0, dx=+100px, assert path=segments[0].x
// Formula: "drag <targetName> by +<dx>px updates <assertPath>"
const SPEC_GEN_TEST_NAME = 'drag seg-0 by +100px updates segments[0].x';
const ASSERT_PATH = 'segments[0].x';

// ---------------------------------------------------------------------------
// Helper: build a minimal snapshotProvider seeded to the spec's expectedFinalState
// ---------------------------------------------------------------------------

function providerFromExpected(spec: GeneratedSpec): SnapshotProvider {
  const snapshot: Record<string, unknown> = {};
  for (const { path, value } of spec.expectedFinalState) {
    snapshot[path] = value;
  }
  return () => snapshot;
}

function providerWithDelta(spec: GeneratedSpec, delta: number): SnapshotProvider {
  const snapshot: Record<string, unknown> = {};
  for (const { path, value } of spec.expectedFinalState) {
    snapshot[path] = (value as number) + delta;
  }
  return () => snapshot;
}

